def _normalize_text(text: str) -> str:
    """
    Базовая нормализация текста:
    - приведение к нижнему регистру
    - удаление лишних пробелов
    """
    text = text.lower().strip()
    text = re.sub(r"\s+", " ", text)
    return text


def _tokenize(text: str) -> List[str]:
    """
    Простая токенизация:
    оставляем слова и числа, игнорируем знаки препинания.
    Для русского текста этого обычно достаточно для базовых метрик.
    """
    text = _normalize_text(text)
    return re.findall(r"\w+", text, flags=re.UNICODE)


def _ngrams(tokens: List[str], n: int) -> List[Tuple[str, ...]]:
    if len(tokens) < n:
        return []
    return [tuple(tokens[i:i+n]) for i in range(len(tokens) - n + 1)]


def _ttr(tokens: List[str]) -> float:
    if not tokens:
        return 0.0
    return len(set(tokens)) / len(tokens)


def _unique_ngram_ratio(tokens: List[str], n: int) -> float:
    grams = _ngrams(tokens, n)
    if not grams:
        return 0.0
    return len(set(grams)) / len(grams)


def _levenshtein_distance_words(tokens1: List[str], tokens2: List[str]) -> int:
    """
    Расстояние Левенштейна по словам.
    """
    m, n = len(tokens1), len(tokens2)

    if m == 0:
        return n
    if n == 0:
        return m

    dp = [[0] * (n + 1) for _ in range(m + 1)]

    for i in range(m + 1):
        dp[i][0] = i
    for j in range(n + 1):
        dp[0][j] = j

    for i in range(1, m + 1):
        for j in range(1, n + 1):
            cost = 0 if tokens1[i - 1] == tokens2[j - 1] else 1
            dp[i][j] = min(
                dp[i - 1][j] + 1,      # удаление
                dp[i][j - 1] + 1,      # вставка
                dp[i - 1][j - 1] + cost  # замена
            )

    return dp[m][n]


def _levenshtein_similarity_words(tokens1: List[str], tokens2: List[str]) -> float:
    """
    Нормированная similarity:
    1.0 — тексты идентичны
    0.0 — максимально различны
    """
    max_len = max(len(tokens1), len(tokens2))
    if max_len == 0:
        return 1.0

    dist = _levenshtein_distance_words(tokens1, tokens2)
    return 1 - dist / max_len


def _safe_mean(values: List[float]) -> float:
    return float(mean(values)) if values else 0.0


def _safe_std(values: List[float]) -> float:
    return float(pstdev(values)) if len(values) > 1 else 0.0


def _summary(values: List[float]) -> Dict[str, float]:
    if not values:
        return {
            "mean": 0.0,
            "std": 0.0,
            "min": 0.0,
            "max": 0.0,
        }
    return {
        "mean": float(mean(values)),
        "std": float(pstdev(values)) if len(values) > 1 else 0.0,
        "min": float(min(values)),
        "max": float(max(values)),
    }


def evaluate_text_corpus(
    texts: List[str],
    bert_model: str = "bert-base-multilingual-cased",
    batch_size: int = 8,
    device: str | None = None,
) -> Dict[str, Any]:
    """
    Оценивает список текстов как корпус одного типа (например, один раздел за разные годы/регионы).
    Метрики считаются попарно по всем уникальным парам текстов.

    Параметры:
        texts: список текстов
        bert_model: модель для BERTScore
        batch_size: размер батча для BERTScore
        device: 'cpu', 'cuda' или None

    Возвращает:
        словарь со сводными метриками.
    """
    if len(texts) < 2:
        raise ValueError("Для расчёта попарных метрик нужно минимум 2 текста.")

    normalized_texts = [_normalize_text(t) for t in texts]
    tokenized_texts = [_tokenize(t) for t in normalized_texts]

    # ---------- Индивидуальные метрики по каждому тексту ----------
    ttr_values = [_ttr(tokens) for tokens in tokenized_texts]
    unique_bigram_ratio_values = [_unique_ngram_ratio(tokens, 2) for tokens in tokenized_texts]
    unique_trigram_ratio_values = [_unique_ngram_ratio(tokens, 3) for tokens in tokenized_texts]
    lengths_tokens = [len(tokens) for tokens in tokenized_texts]

    # ---------- Попарные метрики ----------
    rouge_l_values = []
    bleu_values = []
    levenshtein_similarity_values = []

    scorer = rouge_scorer.RougeScorer(["rougeL"], use_stemmer=False)
    smoothie = SmoothingFunction().method1

    pair_indices = list(combinations(range(len(normalized_texts)), 2))
    bert_refs = []
    bert_cands = []

    for i, j in pair_indices:
        text_i = normalized_texts[i]
        text_j = normalized_texts[j]
        tokens_i = tokenized_texts[i]
        tokens_j = tokenized_texts[j]

        # ROUGE-L (симметризуем через среднее двух направлений)
        rouge_ij = scorer.score(text_i, text_j)["rougeL"].fmeasure
        rouge_ji = scorer.score(text_j, text_i)["rougeL"].fmeasure
        rouge_l = (rouge_ij + rouge_ji) / 2
        rouge_l_values.append(float(rouge_l))

        # BLEU (симметризуем)
        bleu_ij = sentence_bleu([tokens_j], tokens_i, smoothing_function=smoothie)
        bleu_ji = sentence_bleu([tokens_i], tokens_j, smoothing_function=smoothie)
        bleu = (bleu_ij + bleu_ji) / 2
        bleu_values.append(float(bleu))

        # Levenshtein similarity по словам
        lev_sim = _levenshtein_similarity_words(tokens_i, tokens_j)
        levenshtein_similarity_values.append(float(lev_sim))

        # Для BERTScore подготовим пары
        bert_cands.append(text_i)
        bert_refs.append(text_j)

    # ---------- BERTScore ----------
    # Считаем в одном направлении по всем парам, а затем в обратном и усредняем.
    P1, R1, F1_1 = bertscore_score(
        bert_cands,
        bert_refs,
        model_type=bert_model,
        batch_size=batch_size,
        device=device,
        lang="ru",
        verbose=False,
    )

    P2, R2, F1_2 = bertscore_score(
        bert_refs,
        bert_cands,
        model_type=bert_model,
        batch_size=batch_size,
        device=device,
        lang="ru",
        verbose=False,
    )

    bert_precision_values = ((P1.numpy() + P2.numpy()) / 2).tolist()
    bert_recall_values = ((R1.numpy() + R2.numpy()) / 2).tolist()
    bert_f1_values = ((F1_1.numpy() + F1_2.numpy()) / 2).tolist()

    # ---------- Возврат ----------
    result = {
        "corpus_info": {
            "num_texts": len(texts),
            "num_pairs": len(pair_indices),
            "token_lengths": _summary(lengths_tokens),
        },
        "text_level_metrics": {
            "ttr": _summary(ttr_values),
            "unique_bigram_ratio": _summary(unique_bigram_ratio_values),
            "unique_trigram_ratio": _summary(unique_trigram_ratio_values),
        },
        "pairwise_metrics": {
            "rouge_l": _summary(rouge_l_values),
            "bleu": _summary(bleu_values),
            "bertscore_precision": _summary(bert_precision_values),
            "bertscore_recall": _summary(bert_recall_values),
            "bertscore_f1": _summary(bert_f1_values),
            "levenshtein_similarity": _summary(levenshtein_similarity_values),
        },
    }

    return result


from typing import Any, Dict


def print_metrics_report(metrics: Dict[str, Any], digits: int = 4) -> None:
    """
    Красиво печатает словарь, возвращаемый evaluate_text_corpus().
    
    Параметры:
        metrics: словарь с метриками
        digits: число знаков после запятой
    """

    def fmt(value: Any) -> str:
        if isinstance(value, float):
            return f"{value:.{digits}f}"
        return str(value)

    def print_summary_block(title: str, block: Dict[str, Any], indent: int = 0) -> None:
        prefix = " " * indent
        print(f"{prefix}{title}")
        print(f"{prefix}" + "-" * len(title))

        for key, value in block.items():
            if isinstance(value, dict) and {"mean", "std", "min", "max"} <= set(value.keys()):
                print(f"{prefix}{key}:")
                print(f"{prefix}  mean = {fmt(value['mean'])}")
                print(f"{prefix}  std  = {fmt(value['std'])}")
                print(f"{prefix}  min  = {fmt(value['min'])}")
                print(f"{prefix}  max  = {fmt(value['max'])}")
            else:
                print(f"{prefix}{key}: {fmt(value)}")
        print()

    print("\n" + "=" * 70)
    print("ОТЧЁТ ПО МЕТРИКАМ КОРПУСА ТЕКСТОВ")
    print("=" * 70)

    corpus_info = metrics.get("corpus_info", {})
    if corpus_info:
        print_summary_block("1. Общая информация о корпусе", corpus_info)

    text_level_metrics = metrics.get("text_level_metrics", {})
    if text_level_metrics:
        print_summary_block("2. Метрики на уровне отдельных текстов", text_level_metrics)

    pairwise_metrics = metrics.get("pairwise_metrics", {})
    if pairwise_metrics:
        print_summary_block("3. Попарные метрики сходства текстов", pairwise_metrics)

    print("=" * 70)

